#include "push_swap.h"

void	ft_astai(int **a, char *av)
{
	char	tmp[12];
	int		i;
	int		j;

	j = -1;
	while (*av)
	{
		i = -1;
		while ((*av >= 48 && *av <= 48) | *av == 45)
		{
			tmp[++i] = *av;
			av++;
		}
		tmp[++i] = 0;
		*a[++j] = ft_atoi(tmp);
		av++;
	}
}
