#include "push_swap.h"

void	ft_lisa(int *a, int len_a)
{
	int	lis;
	int	tmp;
	int	i;

	lis = 1;
	tmp = *a;
	i = 0;
	while (++i < len_a)
	{
		if (a[i] > tmp)
		{
			tmp = a[i];
			lis++;
		}
	}
}
