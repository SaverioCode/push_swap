#include "moves.h"

void	ft_sb(t_stack *s, int flag)
{
	int	tmp;

	if (s->len_b < 2)
		return ;
	tmp = s->b[0];
	s->b[0] = s->b[1];
	s->b[1] = tmp;
	if (flag == 1)
		write(1, "sb\n", 3);
}
